package ru.koalexse.aichallenge.agent.profile

/**
 * Провайдер блока профиля пользователя для system-промпта.
 *
 * Вызывается при каждом запросе в `buildMessageList` — данные профиля
 * всегда актуальны, даже если пользователь сменил активный профиль в ходе сессии.
 *
 * Добавляется первым блоком в system-промпт, до `defaultSystemPrompt`
 * и до дополнительных сообщений стратегии.
 *
 * Пример формируемого блока:
 * ```
 * ## User Profile
 * - Имя: Алексей
 * - Цель: учить Kotlin
 * - Локация: Москва
 * ```
 */
interface ProfileSystemPromptProvider {

    /**
     * Возвращает отформатированный блок для system-промпта,
     * или `null` если нечего добавить (нет активного профиля / факты пусты).
     */
    suspend fun getProfileBlock(): String?
}
